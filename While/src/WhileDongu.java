public class WhileDongu {
    public static void main(String[] args) {
        int i=1,k;

        while(i<=5){

            System.out.println(i);
            k=1;
            while (k<=10){
                System.out.print(k+",");
                k++;
            }
            System.out.println("");
            i++;
        }
    }
}
